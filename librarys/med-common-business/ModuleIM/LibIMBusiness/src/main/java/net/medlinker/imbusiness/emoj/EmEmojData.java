    private static EmEaGroupEntity createData() {
        EmEaGroupEntity emojiconGroupEntity = new EmEaGroupEntity();
        EmojiconEntity[] datas = new EmojiconEntity[icons.length];
        for (int i = 0; i < icons.length; i++) {
            datas[i] = new EmojiconEntity(icons[i], emojis[i], EmojiconEntity.Type.NORMAL);
        }
        emojiconGroupEntity.setEmojiconList(Arrays.asList(datas));
        emojiconGroupEntity.setName(ModuleIMBusinessManager.INSTANCE.getApplication()
                .getResources().getString(R.string.em_default_text));
        emojiconGroupEntity.setType(EmojiconEntity.Type.NORMAL);
        return emojiconGroupEntity;
    }


